-- Обновляем всех существующих игроков до новой RPS системы
-- Сбрасываем рейтинг до 100 для всех игроков, которые не играли турниры
UPDATE players 
SET elo_rating = 100 
WHERE games_played = 0;

-- Также обновляем игроков, которые играли, но их рейтинг был в старой системе (выше 500)
-- Оставляем им рейтинг пропорциональный их успехам, но в новом диапазоне
UPDATE players 
SET elo_rating = GREATEST(100, elo_rating / 10)
WHERE games_played > 0 AND elo_rating > 500;